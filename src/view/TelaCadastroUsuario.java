    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        String idString = txtId.getText();
        String nome = txtNome.getText().trim();
        String cpf = txtCpf.getText().trim();
        String email = txtEmail.getText().trim();

        if (nome.isEmpty() || cpf.isEmpty() || idString.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Selecione um usuário da tabela e preencha nome, CPF e Email.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int id = Integer.parseInt(idString);

            UsuarioDTO objUsuarioDTO = new UsuarioDTO();
            objUsuarioDTO.setId(id);
            objUsuarioDTO.setNome(nome);
            objUsuarioDTO.setCpf(cpf);
            objUsuarioDTO.setEmail(email);

            UsuarioDAO objUsuarioDAO = new UsuarioDAO();
            objUsuarioDAO.alterarUsuario(objUsuarioDTO);

            JOptionPane.showMessageDialog(this, "Usuário alterado com sucesso!");

            limparCampos();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Erro: ID inválido.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void tblUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuariosMouseClicked
        // TODO add your handling code here:
        int linhaSelecionada = tblUsuarios.getSelectedRow();
        if (linhaSelecionada == -1) {
            return;
        }
        DefaultTableModel modelo = (DefaultTableModel) tblUsuarios.getModel();

        String id = modelo.getValueAt(linhaSelecionada, 0).toString();
        String nome = modelo.getValueAt(linhaSelecionada, 1).toString();
        String cpf = modelo.getValueAt(linhaSelecionada, 2).toString();
        String email = modelo.getValueAt(linhaSelecionada, 3).toString();

        txtId.setText(id);
        txtNome.setText(nome);
        txtCpf.setText(cpf);
        txtEmail.setText(email);

        btnAlterar.setEnabled(true);
        btnCadastrar.setEnabled(false);
    }//GEN-LAST:event_tblUsuariosMouseClicked

