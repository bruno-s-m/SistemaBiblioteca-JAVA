    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:                                         
        String nome = txtNome.getText().trim();
        String cpf = txtCpf.getText().trim();
        String email = txtEmail.getText().trim();

        if (nome.isEmpty() || cpf.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nome e CPF são obrigatórios!", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
            return;
        }

        UsuarioDTO objUsuarioDTO = new UsuarioDTO();
        objUsuarioDTO.setNome(nome);
        objUsuarioDTO.setCpf(cpf);
        objUsuarioDTO.setEmail(email);

        UsuarioDAO objUsuarioDAO = new UsuarioDAO();
        objUsuarioDAO.cadastrarUsuario(objUsuarioDTO);

        JOptionPane.showMessageDialog(this, "Usuário cadastrado com sucesso!");

        listarUsuariosNaTabela();
        limparCampos();

    }//GEN-LAST:event_btnCadastrarActionPerformed

