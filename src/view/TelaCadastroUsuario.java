    private void listarUsuariosNaTabela() {
        try {
            UsuarioDAO objUsuarioDAO = new UsuarioDAO();
            DefaultTableModel modelo = (DefaultTableModel) tblUsuarios.getModel();
            modelo.setRowCount(0);

            ArrayList<UsuarioDTO> lista = objUsuarioDAO.listarUsuarios();

            for (UsuarioDTO usuario : lista) {
                modelo.addRow(new Object[]{
                    usuario.getId(),
                    usuario.getNome(),
                    usuario.getCpf(),
                    usuario.getEmail()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao listar usuários: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

