    private void tblLivrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLivrosMouseClicked
        // TODO add your handling code here:
        int linhaSelecionada = tblLivros.getSelectedRow();
        if (linhaSelecionada == -1) {
            return;
        }

        DefaultTableModel modelo = (DefaultTableModel) tblLivros.getModel();

        String id = modelo.getValueAt(linhaSelecionada, 0).toString();
        String titulo = modelo.getValueAt(linhaSelecionada, 1).toString();
        String autor = modelo.getValueAt(linhaSelecionada, 2).toString();

        txtId.setText(id);
        txtTitulo.setText(titulo);
        txtAutor.setText(autor);

        btnAlterar.setEnabled(true);
        btnCadastrar.setEnabled(false);
    }//GEN-LAST:event_tblLivrosMouseClicked

