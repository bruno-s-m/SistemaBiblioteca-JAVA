    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        String idString = txtId.getText();
        String titulo = txtTitulo.getText().trim();
        String autor = txtAutor.getText().trim();

        if (titulo.isEmpty() || autor.isEmpty() || idString.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Selecione um livro da tabela e preencha Título e Autor.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int id = Integer.parseInt(idString);

            LivroDTO objLivroDTO = new LivroDTO();
            objLivroDTO.setId(id);
            objLivroDTO.setTitulo(titulo);
            objLivroDTO.setAutor(autor);

            LivroDAO objLivroDAO = new LivroDAO();
            objLivroDAO.alterarLivro(objLivroDTO);

            JOptionPane.showMessageDialog(this, "Livro alterado com sucesso!");

            limparCampos();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Erro: ID inválido.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

