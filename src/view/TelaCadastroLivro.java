    private void listarLivrosNaTabela() {
        try {
            LivroDAO objLivroDAO = new LivroDAO();
            DefaultTableModel modelo = (DefaultTableModel) tblLivros.getModel();
            modelo.setRowCount(0);

            ArrayList<LivroDTO> lista = objLivroDAO.listarLivros();

            for (LivroDTO livro : lista) {
                modelo.addRow(new Object[]{
                    livro.getId(),
                    livro.getTitulo(),
                    livro.getAutor(),
                    livro.isDisponivel()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao listar livros: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

