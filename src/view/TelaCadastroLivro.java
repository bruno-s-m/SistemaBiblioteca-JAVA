    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        String titulo = txtTitulo.getText().trim();
        String autor = txtAutor.getText().trim();

        if (titulo.isEmpty() || autor.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Título e Autor são obrigatórios!", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
            return;
        }

        LivroDTO objLivroDTO = new LivroDTO();
        objLivroDTO.setTitulo(titulo);
        objLivroDTO.setAutor(autor);
        objLivroDTO.setDisponivel(true);

        LivroDAO objLivroDAO = new LivroDAO();
        objLivroDAO.cadastrarLivro(objLivroDTO);

        JOptionPane.showMessageDialog(this, "Livro cadastrado com sucesso!");

        listarLivrosNaTabela();
        limparCampos();
    }//GEN-LAST:event_btnCadastrarActionPerformed

