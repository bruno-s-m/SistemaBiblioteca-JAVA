    private void btnEmprestarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEmprestarActionPerformed
        // TODO add your handling code here:
        try {
            int idUsuario = Integer.parseInt(txtIdUsuario.getText());
            int idLivro = Integer.parseInt(txtIdLivro.getText());

            EmprestimoDTO obj = new EmprestimoDTO();
            obj.setIdUsuario(idUsuario);
            obj.setIdLivro(idLivro);

            EmprestimoDAO dao = new EmprestimoDAO();
            dao.registrarEmprestimo(obj);

            txtIdUsuario.setText("");
            txtIdLivro.setText("");
            listarHistorico();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "IDs não encontrado.");
        }

        listarHistorico();
    }//GEN-LAST:event_btnEmprestarActionPerformed

    private void btnDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDevolverActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tblHistorico.getSelectedRow();

        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um empréstimo na tabela para devolver.");
            return;
        }

        int idEmprestimo = Integer.parseInt(tblHistorico.getValueAt(linhaSelecionada, 0).toString());
        int idLivro = Integer.parseInt(tblHistorico.getValueAt(linhaSelecionada, 2).toString());

        String status = tblHistorico.getValueAt(linhaSelecionada, 5).toString();
        if (!status.equals("Pendente")) {
            JOptionPane.showMessageDialog(this, "Este livro já foi devolvido!");
            return;
        }

        EmprestimoDAO dao = new EmprestimoDAO();
        dao.registrarDevolucao(idEmprestimo, idLivro);
        listarHistorico();
    }//GEN-LAST:event_btnDevolverActionPerformed

