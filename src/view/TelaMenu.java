    private void listarHistorico() {
        try {
            EmprestimoDAO dao = new EmprestimoDAO();

            DefaultTableModel modelo = (DefaultTableModel) tblHistorico.getModel();
            modelo.setRowCount(0);

            ArrayList<EmprestimoDTO> lista = dao.listarHistorico();

            for (EmprestimoDTO emp : lista) {
                modelo.addRow(new Object[]{
                    emp.getId(),
                    emp.getNomeUsuario(),
                    emp.getIdLivro(),
                    emp.getTituloLivro(),
                    emp.getDataEmprestimo(),
                    emp.getDataDevolucao() == null ? "Pendente" : emp.getDataDevolucao()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao listar histórico: " + e.getMessage());
        }
    }
